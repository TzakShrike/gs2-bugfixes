.psx
.align 4

.openfile VA11.BIN, 0x8010DC50

;Minotaurus Name
.org 0x8013F470
.area 0x8013F47F-.
minotaurus:
	.byte 0x47, 0x19, 0x1E, 0x1F, 0x24, 0x11, 0x25, 0x22, 0x25, 0x23, 0x00
.endarea

;Magus Name
.org 0x80141F64
.area 0x80141F73-.
magus:
	.byte 0x47, 0x11, 0x17, 0x25, 0x23, 0x00, 0x00, 0x00, 0x00
.endarea

;Chimera Name
.org 0x80145548
.area 0x80145557-.
Chimera:
	.byte 0x3D, 0x18, 0x19, 0x1D, 0x15, 0x22, 0x11, 0x00, 0x00
.endarea

;Add Chimera Parties to Encounter Table
.org 0x801363D4
.area 0x801363FC-.
encounters:
	.word 0x801362D4, 0x8013637C, 0x801362F0, 0x80136398, 0x8013630C, 0x80136328,
	.word 0x801363B4, 0x80136344, 0x80136344, 0x80136360
.endarea


.close